namespace Gusanito_V1._0
{
    class MapPoints
    {
        private int x;
        private int y;

        public void set(int setx, int sety)
        {
            x = setx;
            y = sety;
        }
        public int getx()
        {
            return x;
        }
        public int gety()
        {
            return y;
        }
    }
}
